    private void connectToServer(String act, String userName, String password) {
        try {
            
            JSONObject info = new JSONObject();
            info.put("action", act);
            info.put("userName", userName);
            info.put("password", password);
            
                writerToServer = new PrintStream(socketToServer.getOutputStream());
                writerToServer.println(info.toString());
                Scanner scanner = new Scanner(socketToServer.getInputStream());
                JSONObject response = new JSONObject(scanner.nextLine());
                String action = response.getString("action");
                
                if (action.equals("register") || action.equals("login")) {
                    String message = response.getString("msg");
                    showMessage(message);
                    boolean success = response.getBoolean("success");
                    if (!success) return;
                    Singleton.id = response.getInt("id");
                    Singleton.userName = userName;
                    Singleton.onlines = response.getJSONArray("array");
                    dispose();
                    new MainForm(writerToServer, socketToServer, response.getJSONArray("savedMessages")).setVisible(true);
                }
                
            } catch (Exception e) {
                showError(e);
            }
    }
  
    
