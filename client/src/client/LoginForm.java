    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        // TODO add your handling code here:
        try {
             String name = etUserName.getText().toString();
             String pass = etPassword.getText().toString();
             if (name.isEmpty() || pass.isEmpty()) {
                 showError("The fields couldn't be empty!");
                 return;
             }
             socketToServer = new Socket("localhost", 12345);
             if (socketToServer.isConnected())
                 connectToServer("register", name, pass);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Unknown error: " + e.toString(), "Error", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnRegisterActionPerformed

